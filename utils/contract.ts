const getSigner = (key: string) => {
  const provider = new ethers.providers.AlchemyProvider(
    "maticmum",
    process.env.ALCHEMY_API_KEY
  );
  const wallet = ethers.Wallet.fromMnemonic(key);
  const signer = wallet.connect(provider);
  return signer;
};

const getContract = (type: string) => {
  let address, abi;

  if (type === "casino") {
    abi = casinoABI;
    address = casinoAddress;
  } else if (type === "token") {
    abi = tokenABI;
    address = tokenAddress;
  }

  if (!abi || !address) return;

  const mnemonic = process.env.MNEMONIC;

  if (!mnemonic || mnemonic.length <= 0) {
    console.log("no seed phrase");
    return;
  }

  const signer = getSigner(mnemonic);
  return new ethers.Contract(address, abi, signer);
};

export { getSigner, getContract };
